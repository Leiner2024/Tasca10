# Definimos una función llamada 'crear_repetits' para crear una cadena repetida 'n' veces
def crear_repetits(n, c):
    return c * n

# En el programa principal:
# Llamamos a la función 'crear_repetits' para crear una cadena repetida 6 veces con el carácter "a"
resultado = crear_repetits(6, "a")

# Mostramos el resultado
print(resultado)

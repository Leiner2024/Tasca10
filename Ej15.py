# Definimos una función llamada 'calcular_longitud' para casualmente calcular la longitud de una lista
def calcular_longitud(y):
    return len(y)

# Creamos una lista.
lista = [1, 99, 44, 55, 77]

# Llamamos a la función 'calcular_longitud' para obtener la longitud de la lista
longitud_de_lista = calcular_longitud(lista)

# Mostramos el resultado
print(f"La longitud de la lista es: {longitud_de_lista}")
